function calculator(num1, num2, operator) {
    
    let number;

    if (operator = '+'){
        return number = num1 + num2;
    }
    
    if (operator = '-'){
        return number = num1 - num2;
    } 
    
    if (operator = '*'){
        return number = num1 * num2;
    } 
    
    if (operator = '/'){
        return number = num1 / num2;
    }
}

module.exports = calculator;
